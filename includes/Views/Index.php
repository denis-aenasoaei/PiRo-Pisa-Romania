                    <h2>Next Steps</h2>
                    <hr>
                    <h1>PISA 2021</h1>
                    <p>This years focus is mathematics with an additional test in <b><i>creative thinking</i></b>. The new
                        <a href="https://pisa2021-maths.oecd.org/">PISA 2021 mathematics framework</a> was recently launched.<br><br>
                        Preparations for this test are underway with participants from 36 OECD members and likely 50+ non-members involved.
                    </p>
                    <h1>PISA 2024</h1>
                    <p>The PISA 2024 innovative domain, <b><i>Learning in the Digital World</i></b>, aims to measure students’ ability to engage in self-regulated
                        learning while using digital tools. The tasks measure students’ capacity to monitor their progress, use feedback and stay
                        engaged while they build models and develop solutions with digital tools. These skillsets are essential in preparing students
                        to learn autonomously in an increasingly complex and digital world.</p>
